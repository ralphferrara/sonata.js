      $.init('ModalSnackBar', class { 

            /*||=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-||
            //|| Init
            //||=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-||*/

            init() {
                  $('#modalSnackBar ul').on("click", () =>{ $('#modalSnackBar ul li').remove(); });
            }

            /*||=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-||
            //|| Show
            //||=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-||*/

            create(message, options = {}) {
                  console.log("ModalSnackBar::create(" + message + ")");
                  const duration = (typeof(options.duration) === 'undefined') ? 5000      : options.duration;
                  const type     = (typeof(options.type)     === 'undefined') ? 'DEFAULT' : options.type.toUpperCase();
                  $('#modalSnackBar ul').append(this.snackItem(message, type));
                  window.setTimeout(() => { 
                        $('#modalSnackBar ul li').first().fadeOut(1000, () => { 
                              $('#modalSnackBar ul li').first().remove(); 
                        });
                  }, duration);
            };


            /*||=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-||
            //|| Get Template
            //||=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-||*/

            snackItem(message, type) {
                  console.log('type  => ' + type);
                  var myCode = 'DEFAULT';
                  switch(type) { 
                        case "PENDNG"  : myCode = "PENDNG"; break;
                        case "SUCCESS" : myCode = "SUCCESS"; break;
                        case "ERROR"   : myCode = "ERROR"; break;
                  }                 
                  console.log("CODE => " + myCode); 
                  var myTemp = '<li onclick="event.stopPropagation()">' +$('#ModalSnackBar-' + myCode).html() + '</li>';
                  myTemp = myTemp.replace('{{MESSAGE}}',   message);
                  return myTemp;
            }             

            /*||=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-||
            //|| Show
            //||=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-||*/

      }); 

